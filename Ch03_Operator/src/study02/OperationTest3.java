package study02;

public class OperationTest3 {
//비교 연산자 테스트
	public static void main(String[] args) {
		
		int x = 10;
		int y = 5;
		boolean result = false;

		result = x > y;
		System.out.println("x > y = " + result);
		result = x < y;
		System.out.println("x < y = " + result);
		result = x >= y;
		System.out.println("x >= y = " + result);
		result = x <= y;
		System.out.println("x <= y = " + result);
		result = x == y;
		System.out.println("x == y = " + result);
		result = x <= y;
		System.out.println("x <= y = " + result);
	}

}
